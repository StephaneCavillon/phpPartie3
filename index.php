<h1>Partie 3 : Les boucles</h1>


<h2>Exercice 1</h2>

<p>Créer une variable et l'initialiser à 0.  
Tant que cette variable n'atteint pas 10, il faut :
- l'afficher
- l'incrementer</p>

<h2>Exercice 2</h2>

<p>Créer deux variables. Initialiser la première à 0 et la deuxième avec un nombre compris en 1 et 100.  
Tant que la première variable n'est pas supérieure à 20 :
- multiplier la première variable avec la deuxième
- afficher le résultat
- incrementer la première variable</p>

<h2>Exercice 3</h2>

<p>Créer deux variables. Initialiser la première à 100 et la deuxième avec un nombre compris en 1 et 100.  
Tant que la première variable n'est pas inférieure ou égale à 10 :
- multiplier la première variable avec la deuxième
- afficher le résultat
- décrémenter la première variable</p>

<h2>Exercice 4</h2>

<p>Créer une variable et l'initialiser à 1.  
Tant que cette variable n'atteint pas 10, il faut :
- l'afficher
- l'incrementer de la moitié de sa valeur</p>

<h2>Exercice 5</h2>

<p>En allant de 1 à 15 avec un pas de 1, afficher le message On y arrive presque.</p>

<h2>Exercice 6</h2>

<p>En allant de 20 à 0 avec un pas de 1, afficher le message C'est presque bon.</p>

<h2>Exercice 7</h2>

<p>En allant de 1 à 100 avec un pas de 15, afficher le message On tient le bon bout.</p>

<h2>Exercice 8</h2>

<p>En allant de 200 à 0 avec un pas de 12, afficher le message Enfin !!!!.</p>